    private boolean isEqual(char[] password, char[] password0) {
        int n1=password.length,n2=password0.length;
        if(n1==n2)
        {
            for(int i=0;i<n1;i++)
            {
                if(password[i]!=password0[i])
                    return false;
            }
        }
        else
            return false;
        return true;        
    }
